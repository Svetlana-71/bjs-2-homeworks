class Library {
	constructor(name) {
		this.name = name;
		this.books = [];
	}
	addBook(book) {
		if (book.state > 30) {
			this.books.push(book);
		}
	}
	findBookBy(key, value) {
		for (let book of this.books) {
			if (book[key] === value) {
				return book;
			}
		}
		return null;
	}
	giveBookByName(bookName) {
		for (let book of this.books) {
			if (book.name === bookName) {
				return this.books.splice(this.books.indexOf(book), 1)[0];
			}
		}
		return null;
	}
}

